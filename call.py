def add1(x, y):
    return x + y

def sub1(x, y):
    return x - y

def mul1(x, y):
    return x * y

def div1(x, y):
    if y == 0:
        return "Division by zero is not allowed"
    return x / y

def mod1(x, y):
    if y == 0:
        return "Modulo by zero is not allowed"
    return x % y

def pow1(x, y):
    return x ** y
